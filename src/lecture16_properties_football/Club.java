package lecture16_properties_football;

public class Club {
    // attributes
    private int id;
    private String name;


    // properties
    public int getId() {
        return this.id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
